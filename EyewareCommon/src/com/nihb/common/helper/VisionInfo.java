package com.nihb.common.helper;

public class VisionInfo {
	NihbDecimal prism;
	NihbDecimal	base;
	NihbDecimal sphere;
	NihbDecimal cyl;
	NihbDecimal axis;
	NihbDecimal add;
	/**
	 * @return the prism
	 */
	public NihbDecimal getPrism() {
		return prism;
	}
	/**
	 * @param prism the prism to set
	 */
	public void setPrism(NihbDecimal prism) {
		this.prism = prism;
	}
	/**
	 * @return the base
	 */
	public NihbDecimal getBase() {
		return base;
	}
	/**
	 * @param base the base to set
	 */
	public void setBase(NihbDecimal base) {
		this.base = base;
	}
	/**
	 * @return the sphere
	 */
	public NihbDecimal getSphere() {
		return sphere;
	}
	/**
	 * @param sphere the sphere to set
	 */
	public void setSphere(NihbDecimal sphere) {
		this.sphere = sphere;
	}
	/**
	 * @return the cyl
	 */
	public NihbDecimal getCyl() {
		return cyl;
	}
	/**
	 * @param cyl the cyl to set
	 */
	public void setCyl(NihbDecimal cyl) {
		this.cyl = cyl;
	}
	/**
	 * @return the axis
	 */
	public NihbDecimal getAxis() {
		return axis;
	}
	/**
	 * @param axis the axis to set
	 */
	public void setAxis(NihbDecimal axis) {
		this.axis = axis;
	}
	/**
	 * @return the add
	 */
	public NihbDecimal getAdd() {
		return add;
	}
	/**
	 * @param add the add to set
	 */
	public void setAdd(NihbDecimal add) {
		this.add = add;
	}


}
